#!/usr/bin/env python3
"""Corrective-memory experiment (the headline memory result), leakage-controlled.

For each project-specific request we compare three conditions, all tested on the
ORIGINAL request:
  cold      — empty memory (the base model's guess).
  verbatim  — memory seeded with the *exact* prior request → command. This is a
              LOOKUP: the store key equals the test query, so BM25 matches it
              perfectly. We report it only as the mechanism's UPPER BOUND, not as
              the result — a verbatim repeat is a trivial case.
  paraphrase— memory seeded with a *reworded* prior request (same intent, different
              surface form, generated by an independent model) → command. The store
              key is lexically disjoint from the test query, so a warm win requires
              retrieval to BRIDGE the paraphrase gap and the model to apply the
              recalled command to new wording. This is the real corrective-memory
              scenario (a user phrases the same intent differently across sessions)
              and is the number we headline.

All memory is isolated via MARS_CMD_MEMORY — never touches ~/.mars/cmd_memory.jsonl.
Seeds ALL 24 pairs at once, so retrieval must surface the RIGHT one per request.
"""
import os, sys
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from lib import (read_jsonl, write_jsonl, available_models, mars_translate,
                 judge_translation, judge_name, GOLD, RESULTS, limit)


def main():
    # paraphrases.jsonl carries {id, request, paraphrase, gold} for the personalized set.
    gold = limit(read_jsonl(GOLD / "paraphrases.jsonl"))
    models = available_models()
    if not models:
        raise SystemExit("Set GROQ_API_KEY and/or GEMINI_API_KEY.")

    empty = RESULTS / "lift_empty.jsonl";     write_jsonl(empty, [])
    verb = RESULTS / "lift_mem_verbatim.jsonl"
    write_jsonl(verb, [{"request": g["request"], "command": g["gold"]} for g in gold])
    para = RESULTS / "lift_mem_paraphrase.jsonl"
    write_jsonl(para, [{"request": g["paraphrase"], "command": g["gold"]} for g in gold])

    rows = []
    for m in models:
        print(f"# {m['name']}: {len(gold)} personalized tasks — cold / verbatim / paraphrase…", file=sys.stderr)
        for g in gold:
            req = g["request"]
            cold = mars_translate(req, m, memory="none", cmd_memory=empty)
            cv = judge_translation(req, cold, g["gold"])
            wv_cmd = mars_translate(req, m, memory="history", cmd_memory=verb)
            wv = judge_translation(req, wv_cmd, g["gold"])
            wp_cmd = mars_translate(req, m, memory="history", cmd_memory=para)
            wp = judge_translation(req, wp_cmd, g["gold"])
            rows.append({"model": m["name"], "id": g["id"], "request": req,
                         "paraphrase": g["paraphrase"], "gold": g["gold"],
                         "cold": cold, "cold_verdict": cv,
                         "warm": wv_cmd, "warm_verdict": wv,               # verbatim (lookup ceiling)
                         "para": wp_cmd, "para_verdict": wp})              # paraphrase (headline)
            print(f"  [{m['name']}] {g['id']}: cold {cv} | verbatim {wv} | paraphrase {wp}", file=sys.stderr)
    write_jsonl(RESULTS / "lift.jsonl", rows)

    print(f"\nCorrective-memory lift on personalized tasks (judge={judge_name()}):")
    print(f"  {'model':20} {'cold':>6} {'para':>6} {'verb(ceil)':>11} {'cold→para':>10}")
    for m in models:
        mr = [r for r in rows if r["model"] == m["name"]]
        n = max(len(mr), 1)
        ca = sum(r["cold_verdict"] == "correct" for r in mr) / n
        pa = sum(r["para_verdict"] == "correct" for r in mr) / n
        va = sum(r["warm_verdict"] == "correct" for r in mr) / n
        c2p = sum(r["cold_verdict"] != "correct" and r["para_verdict"] == "correct" for r in mr)
        print(f"  {m['name']:20} {ca:>6.0%} {pa:>6.0%} {va:>10.0%} {c2p:>7}/{len(mr)}")


if __name__ == "__main__":
    main()
